package com.ikmo.main.domain;

public class GameDomain {
	private String gameDate;
	
	private String homeTeam;
	private String homeFirstPlayer;
	private String homeSecondPlayer;
	
	private String awayTeam;
	private String awayFirstPlayer;
	private String awaySecondPlayer;
	
	private String homeGameResult;
	private String awayGameResult;
	
	private String stadium;
	
	public String getGameDate() {
		return gameDate;
	}
	public void setGameDate(String gameDate) {
		this.gameDate = gameDate;
	}
	public String getHomeTeam() {
		return homeTeam;
	}
	public void setHomeTeam(String homeTeam) {
		this.homeTeam = homeTeam;
	}
	public String getHomeFirstPlayer() {
		return homeFirstPlayer;
	}
	public void setHomeFirstPlayer(String homeFirstPlayer) {
		this.homeFirstPlayer = homeFirstPlayer;
	}
	public String getHomeSecondPlayer() {
		return homeSecondPlayer;
	}
	public void setHomeSecondPlayer(String homeSecondPlayer) {
		this.homeSecondPlayer = homeSecondPlayer;
	}
	public String getAwayTeam() {
		return awayTeam;
	}
	public void setAwayTeam(String awayTeam) {
		this.awayTeam = awayTeam;
	}
	public String getAwayFirstPlayer() {
		return awayFirstPlayer;
	}
	public void setAwayFirstPlayer(String awayFirstPlayer) {
		this.awayFirstPlayer = awayFirstPlayer;
	}
	public String getAwaySecondPlayer() {
		return awaySecondPlayer;
	}
	public void setAwaySecondPlayer(String awaySecondPlayer) {
		this.awaySecondPlayer = awaySecondPlayer;
	}
	public String getHomeGameResult() {
		return homeGameResult;
	}
	public void setHomeGameResult(String homeGameResult) {
		this.homeGameResult = homeGameResult;
	}
	public String getAwayGameResult() {
		return awayGameResult;
	}
	public void setAwayGameResult(String awayGameResult) {
		this.awayGameResult = awayGameResult;
	}
	public String getStadium() {
		return stadium;
	}
	public void setStadium(String stadium) {
		this.stadium = stadium;
	}
	
	
}
